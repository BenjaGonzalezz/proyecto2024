function desplazarDerecha() {
    const contenedor = document.querySelector('.producto-mosquitero');
    contenedor.scrollLeft += 500; // Ajusta este valor según la cantidad de desplazamiento deseado
  }
  
function desplazarIzquierda() {
    const contenedor = document.querySelector('.producto-mosquitero');
    contenedor.scrollLeft -= 500; // Ajusta este valor según la cantidad de desplazamiento deseado
  }
  